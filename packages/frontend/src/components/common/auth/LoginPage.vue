<script setup lang="ts">
import { useLogo } from '@/composables/useLogo';
import { useAuthStore } from '@/stores/auth';
import { useSnackbarStore } from '@/stores/snackbar';
import validationUtils from '@/utils/validation';
import type { VForm } from 'vuetify/components';

const logo = useLogo();
const loginForm = ref<VForm>();
const email = ref('');
const password = ref('');
const errorMessage = ref<string[] | null>(null);
const { rules } = validationUtils;
const loading = ref(false);
const route = useRoute('/login');
const router = useRouter();

const login = async () => {
  errorMessage.value = null;

  const isValid = await loginForm.value?.validate();
  if (isValid?.valid) {
    try {
      const { login } = useAuthStore();

      loading.value = true;
      await login(email.value, password.value);

      window.location.pathname = route.redirectedFrom?.fullPath ?? '/';
    } catch (error: any) {
      const { showSnackbar } = useSnackbarStore();

      loading.value = false;

      if (error.response.status === 401) {
        errorMessage.value = ['Wrong email or password.'];
      } else {
        showSnackbar({
          message: 'Something went wrong, please try again.',
          color: 'error',
        });
      }
    }
  }
};

const navigateToRegisterPage = () => {
  router.push('/register');
};
</script>

<template>
  <v-container class="fill-height">
    <v-row class="justify-center">
      <v-col cols="12" sm="8" md="6" class="mt-n12">
        <v-img
          :src="logo.getLogoUrlByTheme()"
          alt="tillywork"
          width="225"
          class="mx-auto mb-3"
        />
        <v-form ref="loginForm" @submit.prevent="login">
          <v-card color="accent" class="pa-4">
            <v-card-title class="text-h5 mb-4">Welcome back!</v-card-title>
            <v-card-text>
              <v-text-field
                v-model="email"
                label="Email"
                required
                :rules="[rules.required, rules.email]"
                :error="!!errorMessage?.length"
              ></v-text-field>
              <v-text-field
                v-model="password"
                label="Password"
                type="password"
                required
                :rules="[rules.required]"
                :error-messages="errorMessage"
              ></v-text-field>
            </v-card-text>
            <v-card-actions class="px-4 pt-0">
              <p>
                Don't have an account?
                <span class="link" @click.prevent="navigateToRegisterPage"
                  >Register</span
                >
              </p>
              <v-spacer />
              <v-btn
                type="submit"
                variant="flat"
                color="primary"
                :loading="loading"
                class="text-body-2"
                >Login</v-btn
              >
            </v-card-actions>
          </v-card>
        </v-form>
      </v-col>
    </v-row>
  </v-container>
</template>
